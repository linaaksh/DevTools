import math


def area(r):
    """ Returns circle area with given radius.
    
    parameter r: circle radius
    type r: float

    return: circle area
    return type: float
    """
    return math.pi * r * r


def perimeter(r):
    """ Returns circle perimeter with given radius.
    
    parameter r: circle radius
    type r: float

    return: circle perimeter
    return type: float
    """
    return 2 * math.pi * r

