def area(a, h):
    """ Returns triangle area with given base and height.
    
    parameter a: base
    parameter h: height
    type a: float
    type h: float

    return: triangle area
    return type: float
    """
    return a * h / 2 

def perimeter(a, b, c):
    """ Returns triangle perimeter with given sides.
    
    parameter a: first side
    parameter b: second side
    parameter c: third side
    type a: float
    type b: float
    type c: float

    return: triangle perimeter
    return type: float
    """
    return a + b + c 