def area(a, b): 
    """ Returns rectangle area with given sides.
    
    parameter a: first side
    parameter b: second side
    type a: float
    type b: float

    return: rectangle area
    return type: float
    """
    return a * b 

def perimeter(a, b):
    """ Returns rectangle perimeter with given sides.
    
    parameter a: first side
    parameter b: second side
    type a: float
    type b: float

    return: rectangle area
    return type: float
    """
    return (a + b) * 2